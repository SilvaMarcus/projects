//----WHILE----

let volta = 1 
while(volta <= 5) {
     
    console.log(volta)
    volta++ // Ao final de cada volta, somara 1 a variavel volta 
}

//------ DO WHILE------

let volver = 1 
do{
    console.log(volver)
    volver++

}  while (volver <= 5); // fez o mesmo que o while, mas a condição é declarada em baixo, portanto ele sempre execurata pelo menos uma vez. 

